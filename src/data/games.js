export const getPrevNextDays = ({date, gamesCount}) => {
  const reversedGamesCount = gamesCount.slice().reverse();
  const prevDayIndex = reversedGamesCount.findIndex(
    (game) => date > game.date,
  );
  const nextDayIndex = gamesCount.findIndex(
    (game) => date < game.date,
  );
  return {
    nextDay: nextDayIndex >= 0 ? gamesCount[nextDayIndex].date : null,
    prevDay: prevDayIndex >= 0 ? reversedGamesCount[prevDayIndex].date : null,
  };
};

const gameStages = ["Preseason", "Regular season", "All-star games", "Playoffs"];

export const getSeasonStageName = (date) => {
  const gameDay = gamesCount.find(gameDay => gameDay.date === date);
  return gameDay
    ? gameStages[gameDay.seasonStageId - 1]
    : "";
};

export const getGameIds = (date) => {
  const gameDay = gamesCount.find(gameDay => gameDay.date === date);
  return gameDay
    ? gameDay.gameIds
    : [];
};